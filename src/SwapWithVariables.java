
public class SwapWithVariables {
	
	public static void main(String args[])
	{
		
		
		int a=10;
		int b=20;
		int temp;
		System.out.println("Before swapping the values of a and b are "+ a+" "+b);
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("After swapping the values of a and b are "+ a+" "+b);
	}

}
